<script>
export default {
  name: "AboutPage",
  data() {
    return {
      images: [
        require("../assets/logo1.jpg"),
        require("../assets/logo2.jpg"),
        require("../assets/logo3.jpg"),
        require("../assets/logo4.jpg"),
      ],
      currentIndex: 0,
      timer: null,
      intervalTime: 5000, // 5ç§’
    };
  },
  computed: {
    currentImage() {
      return this.images[this.currentIndex];
    },
  },
  created() {
    this.startTimer();
  },
  // å°‡ destroyed æ”¹ç‚º unmounted
  unmounted() {
    this.stopTimer();
  },
  methods: {
    startTimer() {
      this.timer = setInterval(() => {
        this.changeImage();
      }, this.intervalTime);
    },
    stopTimer() {
      clearInterval(this.timer);
    },
    changeImage() {
      this.currentIndex = (this.currentIndex + 1) % this.images.length;
    },
  },
};
</script>

